var myAge = 26;
// variable defined as number 26;

let earlyYears = 2;
// var as let since will change, val to two

earlyYears *= 10.5;
// multiplied the value by 10.5

let laterYears = myAge - 2;
// Changable let

laterYears *= 4;
// multiplied the value by 4

console.log(earlyYears);
console.log(laterYears);

const myAgeInDogYears = earlyYears + laterYears;
// my Age in dog years

var myName = 'Francisco Mateus';
myName.toLowerCase();
// get my name only to lowercase letters

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);

